package com.caferaters.model;

public class CafeRating {

	private Long cafeId;
	private Long rating;
	
	public Long getCafeId() {
		return cafeId;
	}
	public void setCafeId(Long cafeId) {
		this.cafeId = cafeId;
	}
	public Long getRating() {
		return rating;
	}
	public void setRating(Long rating) {
		this.rating = rating;
	}
}